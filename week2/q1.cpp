#include <iostream>
#include <cmath>
using namespace std; 

int main () { 
    int n;
    cout << "Enter a number: ";
    cin >> n; 

    cout << "Square root of " << fabs(n) << " is: " << sqrt(fabs(n)) << endl;
    cout << "2 raised to the power of 3 = " << pow (2,3)<< endl;
    cout << "Absolute value of " << n << " is: " << fabs(n) << endl;

}